// 计算得分最高的题目评分结果

// eslint-disable-next-line @typescript-eslint/no-shadow
export function calculateScore  (answerList)  {
  // 创建一个对象来存储每个评分结果的得分
  const scores = {};
  console.log("a"+questionResults)
  // 遍历每个评分结果
  questionResults.forEach(result => {
    // 初始化得分为0
    scores[result.resultName] = 0;
    // 遍历每个答案
    answerList.forEach(answer => {
      // 遍历每个问题的选项
      questions.forEach(question => {
        // 如果答案与选项的key匹配，并且选项的result在评分结果的resultProp中，则得分+1
        if (question.options.some(option => option.key === answer && result.resultProp.includes(option.result))) {
          scores[result.resultName]++;
        }
      });
    });
  });
  // 找到得分最高的评分结果
  const maxScore = Math.max(...Object.values(scores));
  const maxScoreResults = questionResults.filter(result => scores[result.resultName] === maxScore)[0];
  return maxScoreResults;
};

